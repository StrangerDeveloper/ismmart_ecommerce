  animatePageController() {
    timer = Timer.periodic(const Duration(seconds: 4), (Timer timer) {
      if (currentPage.value < bannersList.length) {
        currentPage.value++;
      } else {
        currentPage.value = 0;
      }

      if (pageController.hasClients) {
        pageController.animateToPage(
